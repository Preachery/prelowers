const FLAGS = {
    en: '<img src="https://flagcdn.com/w20/gb.png" width="18" alt="EN" style="vertical-align: middle;">',
    tr: '<img src="https://flagcdn.com/w20/tr.png" width="18" alt="TR" style="vertical-align: middle;">',
    es: '<img src="https://flagcdn.com/w20/es.png" width="18" alt="ES" style="vertical-align: middle;">',
    de: '<img src="https://flagcdn.com/w20/de.png" width="18" alt="DE" style="vertical-align: middle;">',
    fr: '<img src="https://flagcdn.com/w20/fr.png" width="18" alt="FR" style="vertical-align: middle;">'
};

document.addEventListener('DOMContentLoaded', () => {
    // PWA Service Worker Registration
    if ('serviceWorker' in navigator) {
        navigator.serviceWorker.register('./sw.js').catch(err => console.log('PWA registration failed:', err));
    }

    const copyBtn = document.getElementById('copyBtn');
    const codeViewer = document.getElementById('code-viewer');
    
    // Modal Elements
    const tutorialModal = document.getElementById('tutorialModal');
    const btnModalClose = document.getElementById('btnModalClose');
    const btnModalIg = document.getElementById('btnModalIg');
    const tutorialVideo = document.getElementById('tutorialVideo');

    // Dropdown Elements
    const dropBtn = document.getElementById('langDropBtn');
    const dropMenu = document.getElementById('langDropMenu');
    const dropItems = document.querySelectorAll('.lang-drop-item');
    
    let scriptContent = '';

    // Initialize Language
    let currentLang = localStorage.getItem('prelowers-lang') || (navigator.language.startsWith('tr') ? 'tr' : 'en');
    updateLanguage(currentLang);

    // Fetch the script content securely
    fetch('./tool.js')
        .then(response => response.text())
        .then(data => {
            scriptContent = data;
            if(codeViewer) {
                codeViewer.textContent = '';
                const lines = scriptContent.split('\n');
                const visibleLines = [];
                let i = 0;
                const speed = 4; // Lines per frame
                
                function typeCode() {
                    if (i < lines.length) {
                        for(let j = 0; j < speed && i < lines.length; j++) {
                            visibleLines.push(lines[i]);
                            i++;
                        }
                        // Keep only last 22 lines to simulate terminal auto-scroll without scrollbars
                        if (visibleLines.length > 22) {
                            visibleLines.splice(0, visibleLines.length - 22);
                        }
                        codeViewer.textContent = visibleLines.join('\n');
                        requestAnimationFrame(typeCode);
                    } else {
                        // Once finished, show the complete code and syntax highlight it!
                        codeViewer.textContent = scriptContent;
                        if(window.Prism) Prism.highlightElement(codeViewer);
                        // Add a slight flash effect for that professional feel
                        codeViewer.style.animation = 'flash 0.5s ease';
                    }
                }
                
                // Start animation with a slight delay
                setTimeout(typeCode, 500);
            }
        })
        .catch(err => console.error('Initialization failed.'));

    // In-button Copy Animation & Modal Open
    if(copyBtn) {
        copyBtn.addEventListener('click', async () => {
            try {
                const scriptUrl = 'tool.js';
                const res = await fetch(scriptUrl);
                let scriptContent = await res.text();
                
                // --- INJECT PRE-SETTINGS ---
                const pLang = document.getElementById('sgLang').value || 'en';
                const pSpeed = document.getElementById('sgSpeed').value || 'normal';
                const pProtect = document.getElementById('sgProtect').checked;
                
                const preSettings = `
// --- PRELOWERS INJECTED SETTINGS ---
window.PRELOWERS_INJECTED_SETTINGS = {
    lang: "${pLang}",
    speed: "${pSpeed}",
    protectVerified: ${pProtect}
};
// -----------------------------------
`;
                scriptContent = preSettings + "\n" + scriptContent;
                
                await navigator.clipboard.writeText(scriptContent);
                copyBtn.classList.add('copied');
                const span = copyBtn.querySelector('span');
                span.innerText = I18N[currentLang].copyBtnSuccess;
                
                // Show the tutorial modal
                if(tutorialModal) {
                    tutorialModal.classList.add('show');
                    if(tutorialVideo) tutorialVideo.play().catch(e => console.log("Video auto-play prevented"));
                }
                
                // Increment Real Copies API
                fetch('https://api.counterapi.dev/v1/prelowers/copies/up')
                    .then(res => res.json())
                    .then(data => {
                        const copyCountEl = document.getElementById('copyCount');
                        if(copyCountEl && data.count) copyCountEl.innerText = data.count.toLocaleString();
                    }).catch(e => console.log('Telemetry offline'));
                
                setTimeout(() => {
                    copyBtn.classList.remove('copied');
                    span.innerText = I18N[currentLang].copyBtn;
                }, 2000);
            } catch (err) {
                console.error('Copy failed', err);
            }
        });
    }

    // Modal Actions
    if(btnModalClose) {
        btnModalClose.addEventListener('click', () => {
            tutorialModal.classList.remove('show');
            if(tutorialVideo) tutorialVideo.pause();
        });
    }
    
    if(btnModalIg) {
        btnModalIg.addEventListener('click', () => {
            // Open Instagram and close modal
            window.open('https://www.instagram.com/', '_blank');
            tutorialModal.classList.remove('show');
            if(tutorialVideo) tutorialVideo.pause();
        });
    }

    // Dropdown Logic
    if(dropBtn) {
        dropBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            dropMenu.classList.toggle('show');
        });
    }

    document.addEventListener('click', (e) => {
        if(dropMenu && dropMenu.classList.contains('show') && !e.target.closest('.lang-dropdown')) {
            dropMenu.classList.remove('show');
        }
    });

    if(dropItems) {
        dropItems.forEach(item => {
            item.addEventListener('click', (e) => {
                const lang = e.currentTarget.getAttribute('data-lang');
                if(!lang) return; // Skip if it's a theme item
                currentLang = lang;
                localStorage.setItem('prelowers-lang', lang);
                updateLanguage(lang);
                if(dropMenu) dropMenu.classList.remove('show');
                
                // Also update the select dropdown in generator
                const sgLang = document.getElementById('sgLang');
                if(sgLang) sgLang.value = lang;
            });
        });
    }

    // Theme Logic
    const themeDropBtn = document.getElementById('themeDropBtn');
    const themeDropMenu = document.getElementById('themeDropMenu');
    const themeItems = document.querySelectorAll('.lang-drop-item[data-theme]');
    
    const savedTheme = localStorage.getItem('prelowers-theme') || 'default';
    document.body.setAttribute('data-theme', savedTheme);
    
    if(themeDropBtn && themeDropMenu) {
        themeDropBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            themeDropMenu.classList.toggle('show');
            if(dropMenu) dropMenu.classList.remove('show');
        });
    }
    
    document.addEventListener('click', (e) => {
        if(themeDropMenu && themeDropMenu.classList.contains('show') && !e.target.closest('.theme-dropdown')) {
            themeDropMenu.classList.remove('show');
        }
    });

    if(themeItems) {
        themeItems.forEach(item => {
            item.addEventListener('click', (e) => {
                const theme = e.currentTarget.getAttribute('data-theme');
                document.body.setAttribute('data-theme', theme);
                localStorage.setItem('prelowers-theme', theme);
                themeDropMenu.classList.remove('show');
            });
        });
    }

    // Bind generator select to language logic
    const sgLangSelect = document.getElementById('sgLang');
    if(sgLangSelect) {
        sgLangSelect.value = currentLang;
        sgLangSelect.addEventListener('change', (e) => {
            const lang = e.target.value;
            currentLang = lang;
            localStorage.setItem('prelowers-lang', lang);
            updateLanguage(lang);
        });
    }

    // FAQ Accordion Logic
    const faqBtns = document.querySelectorAll('.faq-btn');
    faqBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const item = btn.parentElement;
            const content = item.querySelector('.faq-content');
            
            // Close others
            document.querySelectorAll('.faq-item.active').forEach(otherItem => {
                if(otherItem !== item) {
                    otherItem.classList.remove('active');
                    otherItem.querySelector('.faq-content').style.maxHeight = null;
                }
            });

            if (item.classList.contains('active')) {
                item.classList.remove('active');
                content.style.maxHeight = null;
            } else {
                item.classList.add('active');
                content.style.maxHeight = content.scrollHeight + "px";
            }
        });
    });

    function updateLanguage(lang) {
        const dict = I18N[lang] || I18N['en'];
        
        // Update Dropdown Button UI
        if (dropBtn) {
            dropBtn.innerHTML = `${FLAGS[lang]} ${lang.toUpperCase()} <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><polyline points="6 9 12 15 18 9"></polyline></svg>`;
        }

        // Apply translations
        Object.keys(dict).forEach(key => {
            const el = document.getElementById('t-' + key);
            if(el) el.innerHTML = dict[key];
        });
        
        // If button is currently NOT in copied state, update its text
        if(copyBtn && !copyBtn.classList.contains('copied')) {
            const span = copyBtn.querySelector('span');
            if(span) span.innerText = dict.copyBtn;
        }
    }

    // --- CANVAS SCALING ENGINE V2 ---
    function scaleCanvas() {
        const wrapper = document.getElementById('scale-wrapper');
        const canvas = document.getElementById('canvas-workspace');
        if (!wrapper || !canvas) return;
        
        // Disable scaling entirely on mobile devices
        if(window.innerWidth <= 900) {
            canvas.style.transform = 'none';
            return;
        }
        
        // The master canvas is built for 1540x1080
        const CANVAS_W = 1540;
        const CANVAS_H = 1080;
        
        const scaleX = window.innerWidth / CANVAS_W;
        const scaleY = window.innerHeight / CANVAS_H;
        
        // Use the smaller scale to ensure it fits perfectly inside the screen
        const scale = Math.min(scaleX, scaleY) * 0.95;
        
        canvas.style.transform = `scale(${scale})`;
    }

    window.addEventListener('resize', scaleCanvas);
    scaleCanvas();
    // --- TELEMETRY WIDGET LOGIC ---
    const visitCountEl = document.getElementById('visitCount');
    const copyCountEl = document.getElementById('copyCount');
    
    // 1. Fetch and increment Total Visits
    fetch('https://api.counterapi.dev/v1/prelowers/visits/up')
        .then(res => res.json())
        .then(data => {
            if(visitCountEl) {
                if(data.count) visitCountEl.innerText = data.count.toLocaleString();
                else visitCountEl.innerText = "14,208";
            }
        })
        .catch(() => {
            if(visitCountEl) visitCountEl.innerText = "14,208";
        });

    // 2. Fetch current Total Copies
    fetch('https://api.counterapi.dev/v1/prelowers/copies')
        .then(res => res.json())
        .then(data => {
            if(copyCountEl) {
                if(data.count) copyCountEl.innerText = data.count.toLocaleString();
                else copyCountEl.innerText = "5,412";
            }
        })
        .catch(() => {
            if(copyCountEl) copyCountEl.innerText = "5,412";
        });

});
